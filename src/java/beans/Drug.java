

package beans;


public class Drug {
    private String Name;
    private String Price;
    private String Lab;

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public String getPrice() {
        return Price;
    }

    public void setPrice(String Price) {
        this.Price = Price;
    }

    public String getLab() {
        return Lab;
    }

    public void setLab(String lab) {
        this.Lab = lab;
    }
    
    
}
